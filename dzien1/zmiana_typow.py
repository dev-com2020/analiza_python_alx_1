# rzutowanie
liczba = 5
liczba_2 = 2.5
liczba_3 = "10"

print(float(liczba))    # rzutowanie na float
print(int(liczba_2))     # rzutowanie na int
print(int(liczba_3))     # rzutowanie na int
print(list("abc"))         # ['a', 'b', 'c']
print(list((1, 2, 3)))     # [1, 2, 3]

# jakie są typy danych w Pythonie?

# int - liczby całkowite
# float - liczby zmiennoprzecinkowe
# complex - liczby zespolone
# str - łańcuchy znaków
# bool - wartości logiczne
# NoneType - typ None
# list - lista
# tuple - krotka
# set - zbiór
# dict - słownik